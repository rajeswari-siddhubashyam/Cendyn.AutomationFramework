        /// <summary>
        ///     This method will make sure the eProposal Favorite setting is turned on.
        /// </summary>
        public static void TurnProposalFavoriteOn()
        {
            AdminProperties.iFrame_Enter();
            try
            {
                if (!PageObject_AdminProperties_UpdateProperty_Features.RadioButton_eProposalFavorite_On().Selected)
                {
                    Click_RadioButton_eProposalFavorite_On();
                    Click_Button_Update();
                    Logger.WriteInfoMessage("Selected and turned on the eProposal Favorite setting.");
                }
            }
            catch (Exception)
            {
                Logger.WriteFatalMessage("Unable to turn on the eProposal Favorite Setting");
                throw;
            }

            AdminProperties.iFrame_Exit();
        }

        /// <summary>
        ///     This method will make sure the eProposal Favorite setting is turned off.
        /// </summary>
        public static void TurnProposalFavoriteOff()
        {
            AdminProperties.iFrame_Enter();
            try
            {
                if (!PageObject_AdminProperties_UpdateProperty_Features.RadioButton_eProposalFavorite_Off().Selected)
                {
                    Click_RadioButton_eProposalFavorite_Off();
                    Click_Button_Update();
                    Logger.WriteInfoMessage("Selected and turned off the eProposal Favorite setting.");
                }
            }
            catch (Exception)
            {
                Logger.WriteFatalMessage("Unable to turn off the eProposal Favorite Setting");
                throw;
            }

            AdminProperties.iFrame_Exit();
        }
