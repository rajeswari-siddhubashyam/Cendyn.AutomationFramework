        /// <summary>
        ///     This method will verify the first date of the Room Block matches the first day of the event.
        /// </summary>
        /// <param name="date">Date entered on the Compose page.</param>
        public static void VerifyFirstDate(string date)
        {
            var xpath = "//span[contains(@id,'lblDate1')]";

            try
            {
                if (CommonMethod.Driver.FindElement(By.XPath(xpath)).Text == date)
                    Logger.WriteInfoMessage(
                        "The Room Block first date matched the selected First Date on the Compose page.");
                else
                    throw new Exception();
            }
            catch (Exception)
            {
                Logger.WriteWarnMessage(
                    "The Room Block first date did not match the selected First Date on the Compose page.");
                Logger.WriteWarnMessage("Was looking for: " + date);
                Logger.WriteWarnMessage("But found: " + CommonMethod.Driver.FindElement(By.XPath(xpath)).Text);
            }
        }

